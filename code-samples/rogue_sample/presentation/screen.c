void menu_screen(int current_line)
{
    clear();
    char *strings[] = {
        "           GAME  MENU           ",
        "+------------------------------+",
        "|                              |",
        "|          NEW   GAME          |",
        "|          LOAD  GAME          |",
        "|          SCOREBOARD          |",
        "|          EXIT  GAME          |",
        "|                              |",
        "+------------------------------+",
    };
    
    const int width = strlen(strings[0]) / sizeof(strings[0][0]);
    const int height = sizeof(strings) / sizeof(strings[0]);
    
    int row, col;
    getmaxyx(stdscr, row, col);
    
    int shift_x = (col - width) / 2;
    int shift_y = (row - height) / 2;
    
    for (size_t i = 0; (signed) i < height; i++)
    {
        move(shift_y + i, shift_x);
        printw("%s", strings[i]);
    }

    move(shift_y + current_line + 3, shift_x + 5);
    printw("<<<");
    move(shift_y + current_line + 3, shift_x + 24);
    printw(">>>");
}

